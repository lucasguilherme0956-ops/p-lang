# ═══════════════════════════════════════
# Ошибка парсера
# ═══════════════════════════════════════

class ОшибкаПарсера(Exception):
    def __init__(self, сообщеніе, токенъ=None, файлъ=None):
        self.сообщеніе = сообщеніе
        self.токенъ = токенъ
        self.файлъ = файлъ

    def __str__(self):
        мѣсто = ""
        if self.файлъ:
            мѣсто += f"{self.файлъ}, "
        if self.токенъ:
            мѣсто += f"строка {self.токенъ.строка}, позиція {self.токенъ.столбецъ}"
        return f"Ошибка парсера [{мѣсто}]: {self.сообщеніе}"


# ═══════════════════════════════════════
# Парсеръ
# ═══════════════════════════════════════

class Парсеръ:
    """
    Рекурсивный нисходящій парсеръ языка П.
    """

    def __init__(self, токены: List[Токенъ], файлъ: str = '<вводъ>'):
        self.токены = токены
        self.файлъ = файлъ
        self.поз = 0
        self.ошибки: List[ОшибкаПарсера] = []

    # ─── Навигація по токенамъ ─────────

    @property
    def _тек(self) -> Токенъ:
        """Текущій токенъ."""
        if self.поз < len(self.токены):
            return self.токены[self.поз]
        return self.токены[-1]  # КОНЕЦЪ

    def _заглянуть(self, смещеніе: int = 1) -> Токенъ:
        п = self.поз + смещеніе
        if п < len(self.токены):
            return self.токены[п]
        return self.токены[-1]

    def _подвинуть(self) -> Токенъ:
        """Вернуть текущій и подвинуться."""
        т = self._тек
        if self.поз < len(self.токены) - 1:
            self.поз += 1
        return т

    def _совпадаетъ(self, *типы) -> bool:
        return self._тек.типъ in типы

    def _ожидать(self, типъ: ТипТокена, сообщеніе: str = None) -> Токенъ:
        """Потребовать токенъ заданнаго типа. Ошибка, если не совпалъ."""
        if self._тек.типъ == типъ:
            return self._подвинуть()
        if сообщеніе is None:
            сообщеніе = f"Ожидался «{типъ.value}», получено «{self._тек.типъ.value}» ({self._тек.значеніе!r})"
        self._ошибка(сообщеніе)

    def _ошибка(self, сообщеніе: str):
        ош = ОшибкаПарсера(сообщеніе, токенъ=self._тек, файлъ=self.файлъ)
        self.ошибки.append(ош)
        raise ош

    def _пропустить_новыя_строки(self):
        while self._совпадаетъ(ТипТокена.НОВАЯ_СТРОКА):
            self._подвинуть()

    # ═══════════════════════════════════
    # Главная точка входа
    # ═══════════════════════════════════

    def разобрать(self) -> Программа:
        """Разобрать всю программу."""
        прог = Программа(строка=1, тѣло=[])
        self._пропустить_новыя_строки()
        while not self._совпадаетъ(ТипТокена.КОНЕЦЪ):
            инстр = self._инструкція()
            if инстр is not None:
                прог.тѣло.append(инстр)
            self._пропустить_новыя_строки()
        return прог

    # ═══════════════════════════════════
    # Инструкціи (Statements)
    # ═══════════════════════════════════

    def _инструкція(self) -> Optional[Узелъ]:
        """Разобрать одну инструкцію."""
        т = self._тек.типъ

        if т == ТипТокена.ОБЪЯВИТЬ:
            return self._объявленіе()
        elif т == ТипТокена.ПОСТОЯННАЯ:
            return self._постоянная()
        elif т == ТипТокена.ФУНКЦІЯ:
            return self._функція()
        elif т == ТипТокена.КЛАССЪ:
            return self._классъ()
        elif т == ТипТокена.СОЗДАТЕЛЬ:
            return self._создатель()
        elif т == ТипТокена.МЕТОДЪ:
            return self._методъ()
        elif т == ТипТокена.ВОЗВРАТИТЬ:
            return self._возвратъ()
        elif т == ТипТокена.БУДЕ:
            return self._условіе()
        elif т == ТипТокена.ПОКУДА:
            return self._покуда()
        elif т in (ТипТокена.ДЛЯ, ТипТокена.ДЛЯ_КАЖДАГО):
            return self._для()
        elif т == ТипТокена.ПРЕРВАТЬ:
            return self._прервать()
        elif т == ТипТокена.ПРОДОЛЖИТЬ:
            return self._продолжить()
        elif т == ТипТокена.ПРОПУСТИТЬ:
            return self._пропустить()
        elif т == ТипТокена.ПОПЫТКА:
            return self._попытка()
        elif т == ТипТокена.ВОЗБУДИТЬ:
            return self._возбудить()
        elif т == ТипТокена.ПРИВЕЗТИ:
            return self._привезти()
        elif т == ТипТокена.ИЗЪ:
            return self._привезти_изъ()
        else:
            return self._инструкція_выраженіе()

    # ─── Объявленіе / Присваиваніе ────

    def _объявленіе(self) -> Узелъ:
        """объявить имя есть выраженіе"""
        т = self._подвинуть()  # ОБЪЯВИТЬ
        имя_ток = self._ожидать(ТипТокена.ИДЕНТИФИКАТОРЪ,
                                "Послѣ «объявить» ожидается имя перемѣнной")
        self._ожидать(ТипТокена.ЕСТЬ,
                      "Послѣ имени ожидается «есть»")
        значеніе = self._выраженіе()
        self._конецъ_инструкціи()
        return Объявленіе(строка=т.строка, имя=имя_ток.значеніе, значеніе=значеніе)

    def _постоянная(self) -> Узелъ:
        """постоянная ИМЯ есть выраженіе"""
        т = self._подвинуть()  # ПОСТОЯННАЯ
        имя_ток = self._ожидать(ТипТокена.ИДЕНТИФИКАТОРЪ,
                                "Послѣ «постоянная» ожидается имя")
        self._ожидать(ТипТокена.ЕСТЬ,
                      "Послѣ имени ожидается «есть»")
        значеніе = self._выраженіе()
        self._конецъ_инструкціи()
        return Постоянная(строка=т.строка, имя=имя_ток.значеніе, значеніе=значеніе)

    def _инструкція_выраженіе(self) -> Узелъ:
        """
        Выраженіе-инструкція. Можетъ быть:
          — вызовъ функціи: ф(...)
          — присваиваніе: цѣль есть значеніе
        """
        строка = self._тек.строка
        выр = self._выраженіе()

        # Присваиваніе: выраж ЕСТЬ значеніе
        if self._совпадаетъ(ТипТокена.ЕСТЬ):
            self._подвинуть()  # ЕСТЬ
            значеніе = self._выраженіе()
            self._конецъ_инструкціи()
            return Присваиваніе(строка=строка, цѣль=выр, значеніе=значеніе)

        self._конецъ_инструкціи()
        return ВыраженіеИнструкція(строка=строка, выраженіе=выр)

    # ─── Функція ───────────────────────

    def _функція(self) -> Узелъ:
        """функція имя(параметры): тѣло"""
        т = self._подвинуть()  # ФУНКЦІЯ
        имя_ток = self._ожидать(ТипТокена.ИДЕНТИФИКАТОРЪ,
                                "Послѣ «функція» ожидается имя")
        параметры = self._параметры()
        self._ожидать(ТипТокена.ДВОЕТОЧІЕ,
                      "Послѣ параметровъ ожидается «:»")
        тѣло = self._блокъ()
        return ОпредѣленіеФункціи(
            строка=т.строка, имя=имя_ток.значеніе,
            параметры=параметры, тѣло=тѣло)

    # ─── Классъ ────────────────────────

    def _классъ(self) -> Узелъ:
        """классъ Имя [наслѣдуетъ Родитель]: тѣло"""
        т = self._подвинуть()  # КЛАССЪ
        имя_ток = self._ожидать(ТипТокена.ИДЕНТИФИКАТОРЪ,
                                "Послѣ «классъ» ожидается имя класса")
        родитель = None
        if self._совпадаетъ(ТипТокена.НАСЛѢДУЕТЪ):
            self._подвинуть()
            род_ток = self._ожидать(ТипТокена.ИДЕНТИФИКАТОРЪ,
                                    "Послѣ «наслѣдуетъ» ожидается имя родителя")
            родитель = род_ток.значеніе
        self._ожидать(ТипТокена.ДВОЕТОЧІЕ,
                      "Послѣ имени класса ожидается «:»")
        тѣло = self._блокъ()
        return ОпредѣленіеКласса(
            строка=т.строка, имя=имя_ток.значеніе,
            родитель=родитель, тѣло=тѣло)

    # ─── Создатель / Методъ ────────────

    def _создатель(self) -> Узелъ:
        """создатель(параметры): тѣло"""
        т = self._подвинуть()  # СОЗДАТЕЛЬ
        параметры = self._параметры()
        self._ожидать(ТипТокена.ДВОЕТОЧІЕ, "Ожидается «:»")
        тѣло = self._блокъ()
        return Создатель(строка=т.строка, параметры=параметры, тѣло=тѣло)

    def _методъ(self) -> Узелъ:
        """методъ имя(параметры): тѣло"""
        т = self._подвинуть()  # МЕТОДЪ
        имя_ток = self._ожидать(ТипТокена.ИДЕНТИФИКАТОРЪ,
                                "Послѣ «методъ» ожидается имя")
        параметры = self._параметры()
        self._ожидать(ТипТокена.ДВОЕТОЧІЕ, "Ожидается «:»")
        тѣло = self._блокъ()
        return ОпредѣленіеМетода(
            строка=т.строка, имя=имя_ток.значеніе,
            параметры=параметры, тѣло=тѣло)

    # ─── Возвратъ ──────────────────────

    def _возвратъ(self) -> Узелъ:
        т = self._подвинуть()  # ВОЗВРАТИТЬ
        значеніе = None
        if not self._совпадаетъ(ТипТокена.НОВАЯ_СТРОКА, ТипТокена.КОНЕЦЪ,
                                ТипТокена.ВЫСТУПЪ):
            значеніе = self._выраженіе()
        self._конецъ_инструкціи()
        return Возвратъ(строка=т.строка, значеніе=значеніе)

    # ─── Условіе ───────────────────────

    def _условіе(self) -> Узелъ:
        """буде ... [инако буде ...] [иначе ...]"""
        т = self._подвинуть()  # БУДЕ
        условіе = self._выраженіе()
        self._ожидать(ТипТокена.ДВОЕТОЧІЕ, "Послѣ условія ожидается «:»")
        тѣло = self._блокъ()

        вѣтви = [ВѣтвьБуде(условіе=условіе, тѣло=тѣло)]
        иначе_тѣло = None

        while self._совпадаетъ(ТипТокена.ИНАКО_БУДЕ):
            self._подвинуть()
            усл = self._выраженіе()
            self._ожидать(ТипТокена.ДВОЕТОЧІЕ, "Ожидается «:»")
            тл = self._блокъ()
            вѣтви.append(ВѣтвьБуде(условіе=усл, тѣло=тл))

        if self._совпадаетъ(ТипТокена.ИНАЧЕ):
            self._подвинуть()
            self._ожидать(ТипТокена.ДВОЕТОЧІЕ, "Послѣ «иначе» ожидается «:»")
            иначе_тѣло = self._блокъ()

        return Условіе(строка=т.строка, вѣтви=вѣтви, иначе_тѣло=иначе_тѣло)

    # ─── Циклы ─────────────────────────

    def _покуда(self) -> Узелъ:
        т = self._подвинуть()  # ПОКУДА
        условіе = self._выраженіе()
        self._ожидать(ТипТокена.ДВОЕТОЧІЕ, "Послѣ условія ожидается «:»")
        тѣло = self._блокъ()
        return ЦиклъПокуда(строка=т.строка, условіе=условіе, тѣло=тѣло)

    def _для(self) -> Узелъ:
        """для перем въ итер: тѣло  /  для каждаго перем въ итер: тѣло"""
        т = self._подвинуть()  # ДЛЯ или ДЛЯ_КАЖДАГО
        перем = self._ожидать(ТипТокена.ИДЕНТИФИКАТОРЪ,
                              "Послѣ «для» ожидается имя перемѣнной")
        self._ожидать(ТипТокена.ВЪ, "Ожидается «въ»")
        итерируемое = self._выраженіе()
        self._ожидать(ТипТокена.ДВОЕТОЧІЕ, "Ожидается «:»")
        тѣло = self._блокъ()
        return ЦиклъДля(строка=т.строка, перемѣнная=перем.значеніе,
                        итерируемое=итерируемое, тѣло=тѣло)

    def _прервать(self) -> Узелъ:
        т = self._подвинуть()
        self._конецъ_инструкціи()
        return Прервать(строка=т.строка)

    def _продолжить(self) -> Узелъ:
        т = self._подвинуть()
        self._конецъ_инструкціи()
        return Продолжить(строка=т.строка)

    def _пропустить(self) -> Узелъ:
        т = self._подвинуть()
        self._конецъ_инструкціи()
        return Пропустить(строка=т.строка)

    # ─── Попытка / перехватить ─────────

    def _попытка(self) -> Узелъ:
        т = self._подвинуть()  # ПОПЫТКА
        self._ожидать(ТипТокена.ДВОЕТОЧІЕ, "Ожидается «:»")
        тѣло = self._блокъ()

        перехватъ_имя = None
        перехватъ_тѣло = None
        обязательно_тѣло = None

        if self._совпадаетъ(ТипТокена.ПЕРЕХВАТИТЬ):
            self._подвинуть()
            if self._совпадаетъ(ТипТокена.ИДЕНТИФИКАТОРЪ):
                перехватъ_имя = self._подвинуть().значеніе
            self._ожидать(ТипТокена.ДВОЕТОЧІЕ, "Ожидается «:»")
            перехватъ_тѣло = self._блокъ()

        if self._совпадаетъ(ТипТокена.ОБЯЗАТЕЛЬНО):
            self._подвинуть()
            self._ожидать(ТипТокена.ДВОЕТОЧІЕ, "Ожидается «:»")
            обязательно_тѣло = self._блокъ()

        return Попытка(строка=т.строка, тѣло=тѣло,
                       перехватъ_имя=перехватъ_имя,
                       перехватъ_тѣло=перехватъ_тѣло,
                       обязательно_тѣло=обязательно_тѣло)

    def _возбудить(self) -> Узелъ:
        т = self._подвинуть()  # ВОЗБУДИТЬ
        выр = self._выраженіе()
        self._конецъ_инструкціи()
        return Возбудить(строка=т.строка, выраженіе=выр)

    # ─── Модули ────────────────────────

    def _привезти(self) -> Узелъ:
        """привезти модуль [какъ алиасъ]"""
        т = self._подвинуть()  # ПРИВЕЗТИ
        мод = self._ожидать(ТипТокена.ИДЕНТИФИКАТОРЪ, "Ожидается имя модуля")
        алиасъ = None
        if self._совпадаетъ(ТипТокена.КАКЪ):
            self._подвинуть()
            алиасъ = self._ожидать(ТипТокена.ИДЕНТИФИКАТОРЪ,
                                   "Ожидается псевдонимъ").значеніе
        self._конецъ_инструкціи()
        return Привезти(строка=т.строка, модуль=мод.значеніе, алиасъ=алиасъ)

    def _привезти_изъ(self) -> Узелъ:
        """изъ модуль привезти имя [какъ алиасъ]"""
        т = self._подвинуть()  # ИЗЪ
        мод = self._ожидать(ТипТокена.ИДЕНТИФИКАТОРЪ, "Ожидается имя модуля")
        self._ожидать(ТипТокена.ПРИВЕЗТИ, "Ожидается «привезти»")
        имя = self._ожидать(ТипТокена.ИДЕНТИФИКАТОРЪ, "Ожидается имя")
        алиасъ = None
        if self._совпадаетъ(ТипТокена.КАКЪ):
            self._подвинуть()
            алиасъ = self._ожидать(ТипТокена.ИДЕНТИФИКАТОРЪ,
                                   "Ожидается псевдонимъ").значеніе
        self._конецъ_инструкціи()
        return ПривезтиИзъ(строка=т.строка, модуль=мод.значеніе,
                           имя=имя.значеніе, алиасъ=алиасъ)

    # ═══════════════════════════════════
    # Блоки (отступъ / выступъ)
    # ═══════════════════════════════════

    def _блокъ(self) -> List[Узелъ]:
        """Разобрать блокъ: НОВАЯ_СТРОКА ОТСТУПЪ инструкціи ВЫСТУПЪ"""
        self._пропустить_новыя_строки()
        self._ожидать(ТипТокена.ОТСТУПЪ, "Ожидается блокъ съ отступомъ")
        инструкціи = []
        self._пропустить_новыя_строки()
        while not self._совпадаетъ(ТипТокена.ВЫСТУПЪ, ТипТокена.КОНЕЦЪ):
            и = self._инструкція()
            if и is not None:
                инструкціи.append(и)
            self._пропустить_новыя_строки()
        if self._совпадаетъ(ТипТокена.ВЫСТУПЪ):
            self._подвинуть()
        return инструкціи

    def _имя_параметра(self) -> Токенъ:
        """Принять имя параметра (идентификаторъ или «сей»)."""
        if self._совпадаетъ(ТипТокена.ИДЕНТИФИКАТОРЪ, ТипТокена.СЕЙ):
            return self._подвинуть()
        self._ошибка("Ожидается имя параметра")

    def _параметры(self) -> List[str]:
        """Разобрать список параметровъ: (а, б, в)"""
        self._ожидать(ТипТокена.ЛѢВАЯ_СКОБКА, "Ожидается «(»")
        параметры = []
        if not self._совпадаетъ(ТипТокена.ПРАВАЯ_СКОБКА):
            имя = self._имя_параметра()
            параметры.append(имя.значеніе)
            while self._совпадаетъ(ТипТокена.ЗАПЯТАЯ):
                self._подвинуть()
                имя = self._имя_параметра()
                параметры.append(имя.значеніе)
        self._ожидать(ТипТокена.ПРАВАЯ_СКОБКА, "Ожидается «)»")
        return параметры

    def _конецъ_инструкціи(self):
        """Убѣдиться, что инструкція завершена (новая строка / конецъ / выступъ)."""
        if self._совпадаетъ(ТипТокена.НОВАЯ_СТРОКА):
            self._подвинуть()
        elif self._совпадаетъ(ТипТокена.КОНЕЦЪ, ТипТокена.ВЫСТУПЪ):
            pass  # допустимо
        else:
            self._ошибка(
                f"Ожидается конецъ инструкціи, получено «{self._тек.типъ.value}»"
            )

    # ═══════════════════════════════════
    # Выраженія (Expressions)
    # ═══════════════════════════════════

    def _выраженіе(self) -> Узелъ:
        return self._или()

    # ─── или ───────────────────────────

    def _или(self) -> Узелъ:
        лѣвое = self._и()
        while self._совпадаетъ(ТипТокена.ИЛИ):
            оп = self._подвинуть()
            правое = self._и()
            лѣвое = БинарнаяОперація(строка=оп.строка,
                                      лѣвое=лѣвое, операторъ='или', правое=правое)
        return лѣвое

    # ─── и ─────────────────────────────

    def _и(self) -> Узелъ:
        лѣвое = self._не()
        while self._совпадаетъ(ТипТокена.И):
            оп = self._подвинуть()
            правое = self._не()
            лѣвое = БинарнаяОперація(строка=оп.строка,
                                      лѣвое=лѣвое, операторъ='и', правое=правое)
        return лѣвое

    # ─── не ────────────────────────────

    def _не(self) -> Узелъ:
        if self._совпадаетъ(ТипТокена.НЕ):
            оп = self._подвинуть()
            операндъ = self._не()
            return УнарнаяОперація(строка=оп.строка,
                                   операторъ='не', операндъ=операндъ)
        return self._сравненіе()

    # ─── сравненіе ─────────────────────

    _ОПЕРАТОРЫ_СРАВНЕНІЯ = {
        ТипТокена.РАВНО:     'равно',
        ТипТокена.НЕ_РАВНО:  'не равно',
        ТипТокена.МЕНѢЕ:     'менѣе',
        ТипТокена.БОЛѢЕ:     'болѣе',
        ТипТокена.НЕ_БОЛѢЕ:  'не болѣе',
        ТипТокена.НЕ_МЕНѢЕ:  'не менѣе',
    }

    def _сравненіе(self) -> Узелъ:
        лѣвое = self._сложеніе()
        while self._тек.типъ in self._ОПЕРАТОРЫ_СРАВНЕНІЯ:
            оп_т = self._подвинуть()
            правое = self._сложеніе()
            лѣвое = БинарнаяОперація(
                строка=оп_т.строка,
                лѣвое=лѣвое,
                операторъ=self._ОПЕРАТОРЫ_СРАВНЕНІЯ[оп_т.типъ],
                правое=правое)
        return лѣвое

    # ─── сложеніе / вычитаніе ──────────

    def _сложеніе(self) -> Узелъ:
        лѣвое = self._умноженіе()
        while self._совпадаетъ(ТипТокена.ПЛЮСЪ, ТипТокена.МИНУСЪ):
            оп_т = self._подвинуть()
            оп = 'плюсъ' if оп_т.типъ == ТипТокена.ПЛЮСЪ else 'минусъ'
            правое = self._умноженіе()
            лѣвое = БинарнаяОперація(строка=оп_т.строка,
                                      лѣвое=лѣвое, операторъ=оп, правое=правое)
        return лѣвое

    # ─── умноженіе / дѣленіе ───────────

    _ОПЕРАТОРЫ_МНОЖ = {
        ТипТокена.УМНОЖИТЬ:  'умножить',
        ТипТокена.РАЗДѢЛИТЬ: 'раздѣлить',
        ТипТокена.НАЦѢЛО:    'нацѣло',
        ТипТокена.ОСТАТОКЪ:  'остатокъ',
    }

    def _умноженіе(self) -> Узелъ:
        лѣвое = self._унарное()
        while self._тек.типъ in self._ОПЕРАТОРЫ_МНОЖ:
            оп_т = self._подвинуть()
            правое = self._унарное()
            лѣвое = БинарнаяОперація(
                строка=оп_т.строка,
                лѣвое=лѣвое,
                операторъ=self._ОПЕРАТОРЫ_МНОЖ[оп_т.типъ],
                правое=правое)
        return лѣвое

    # ─── унарный минусъ ────────────────

    def _унарное(self) -> Узелъ:
        if self._совпадаетъ(ТипТокена.МИНУСЪ):
            оп = self._подвинуть()
            операндъ = self._унарное()
            return УнарнаяОперація(строка=оп.строка,
                                   операторъ='минусъ', операндъ=операндъ)
        return self._степень()

    # ─── степень ───────────────────────

    def _степень(self) -> Узелъ:
        основаніе = self._постфиксъ()
        if self._совпадаетъ(ТипТокена.ВЪ_СТЕПЕНИ):
            оп = self._подвинуть()
            показатель = self._унарное()  # правоассоціативно
            return БинарнаяОперація(строка=оп.строка,
                                    лѣвое=основаніе, операторъ='въ степени',
                                    правое=показатель)
        return основаніе

    # ─── постфиксъ: вызовъ, индексъ, точка ─

    def _постфиксъ(self) -> Узелъ:
        узелъ = self._атомъ()

        while True:
            if self._совпадаетъ(ТипТокена.ЛѢВАЯ_СКОБКА):
                # Вызовъ: узелъ(арг, арг, ...)
                self._подвинуть()
                арг = self._аргументы()
                self._ожидать(ТипТокена.ПРАВАЯ_СКОБКА, "Ожидается «)»")
                узелъ = ВызовъФункціи(строка=узелъ.строка,
                                      функція=узелъ, аргументы=арг)

            elif self._совпадаетъ(ТипТокена.ЛѢВАЯ_КВАДРАТНАЯ):
                # Индексъ: узелъ[выраж]
                self._подвинуть()
                индексъ = self._выраженіе()
                self._ожидать(ТипТокена.ПРАВАЯ_КВАДРАТНАЯ, "Ожидается «]»")
                узелъ = ДоступъПоИндексу(строка=узелъ.строка,
                                         объектъ=узелъ, индексъ=индексъ)

            elif self._совпадаетъ(ТипТокена.ТОЧКА):
                # Атрибутъ: узелъ.имя
                self._подвинуть()
                имя = self._ожидать(ТипТокена.ИДЕНТИФИКАТОРЪ,
                                    "Послѣ «.» ожидается имя атрибута")
                узелъ = ДоступъКъАтрибуту(строка=узелъ.строка,
                                          объектъ=узелъ, атрибутъ=имя.значеніе)
            else:
                break

        return узелъ

    def _аргументы(self) -> List[Узелъ]:
        """Разобрать списокъ аргументовъ вызова."""
        арг = []
        if not self._совпадаетъ(ТипТокена.ПРАВАЯ_СКОБКА):
            арг.append(self._выраженіе())
            while self._совпадаетъ(ТипТокена.ЗАПЯТАЯ):
                self._подвинуть()
                арг.append(self._выраженіе())
        return арг

    # ─── атомъ ─────────────────────────

    def _атомъ(self) -> Узелъ:
        т = self._тек

        # Цѣлое число
        if т.типъ == ТипТокена.ЦѢЛОЕ:
            self._подвинуть()
            return ЦѣлоеЧисло(строка=т.строка, значеніе=int(т.значеніе))

        # Вещественное число
        if т.типъ == ТипТокена.ВЕЩЕСТВЕННОЕ:
            self._подвинуть()
            return ВещественноеЧисло(строка=т.строка, значеніе=float(т.значеніе))

        # Строка
        if т.типъ == ТипТокена.СТРОКА:
            self._подвинуть()
            return Строка(строка=т.строка, значеніе=т.значеніе)

        # Истина / Ложь
        if т.типъ == ТипТокена.ИСТИНА:
            self._подвинуть()
            return Булево(строка=т.строка, значеніе=True)
        if т.типъ == ТипТокена.ЛОЖЬ:
            self._подвинуть()
            return Булево(строка=т.строка, значеніе=False)

        # Ничто
        if т.типъ == ТипТокена.НИЧТО:
            self._подвинуть()
            return Ничто(строка=т.строка)

        # Сей (self)
        if т.типъ == ТипТокена.СЕЙ:
            self._подвинуть()
            return Идентификаторъ(строка=т.строка, имя='сей')

        # Родительскій (super)
        if т.типъ == ТипТокена.РОДИТЕЛЬСКІЙ:
            self._подвинуть()
            self._ожидать(ТипТокена.ЛѢВАЯ_СКОБКА, "Ожидается «(» послѣ «родительскій»")
            self._ожидать(ТипТокена.ПРАВАЯ_СКОБКА, "Ожидается «)»")
            return Идентификаторъ(строка=т.строка, имя='родительскій')

        # Идентификаторъ
        if т.типъ == ТипТокена.ИДЕНТИФИКАТОРЪ:
            self._подвинуть()
            return Идентификаторъ(строка=т.строка, имя=т.значеніе)

        # Скобки: (выраженіе)
        if т.типъ == ТипТокена.ЛѢВАЯ_СКОБКА:
            self._подвинуть()
            выр = self._выраженіе()
            self._ожидать(ТипТокена.ПРАВАЯ_СКОБКА, "Ожидается «)»")
            return выр

        # Литералъ списка: [эл, эл, ...]
        if т.типъ == ТипТокена.ЛѢВАЯ_КВАДРАТНАЯ:
            return self._литералъ_списка()

        # Литералъ словаря: {кл: зн, ...}
        if т.типъ == ТипТокена.ЛѢВАЯ_ФИГУРНАЯ:
            return self._литералъ_словаря()

        self._ошибка(f"Неожиданный токенъ: «{т.типъ.value}» ({т.значеніе!r})")

    # ─── Литералы коллекцій ────────────

    def _литералъ_списка(self) -> Узелъ:
        т = self._подвинуть()  # [
        элементы = []
        if not self._совпадаетъ(ТипТокена.ПРАВАЯ_КВАДРАТНАЯ):
            элементы.append(self._выраженіе())
            while self._совпадаетъ(ТипТокена.ЗАПЯТАЯ):
                self._подвинуть()
                if self._совпадаетъ(ТипТокена.ПРАВАЯ_КВАДРАТНАЯ):
                    break  # висячая запятая
                элементы.append(self._выраженіе())
        self._ожидать(ТипТокена.ПРАВАЯ_КВАДРАТНАЯ, "Ожидается «]»")
        return Списокъ(строка=т.строка, элементы=элементы)

    def _литералъ_словаря(self) -> Узелъ:
        т = self._подвинуть()  # {
        пары = []
        if not self._совпадаетъ(ТипТокена.ПРАВАЯ_ФИГУРНАЯ):
            кл = self._выраженіе()
            self._ожидать(ТипТокена.ДВОЕТОЧІЕ, "Ожидается «:» въ словарѣ")
            зн = self._выраженіе()
            пары.append((кл, зн))
            while self._совпадаетъ(ТипТокена.ЗАПЯТАЯ):
                self._подвинуть()
                if self._совпадаетъ(ТипТокена.ПРАВАЯ_ФИГУРНАЯ):
                    break
                кл = self._выраженіе()
                self._ожидать(ТипТокена.ДВОЕТОЧІЕ, "Ожидается «:»")
                зн = self._выраженіе()
                пары.append((кл, зн))
        self._ожидать(ТипТокена.ПРАВАЯ_ФИГУРНАЯ, "Ожидается «}»")
        return Словарь(строка=т.строка, пары=пары)


# ═══════════════════════════════════════
