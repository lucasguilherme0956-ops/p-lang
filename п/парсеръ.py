def разобрать_кодъ(кодъ: str, файлъ: str = '<вводъ>') -> Программа:
    """Удобная обёртка: кодъ → АСД."""
    л = Лексеръ(кодъ, файлъ=файлъ)
    токены = л.токенизировать()
    if л.ошибки:
        raise л.ошибки[0]
    п = Парсеръ(токены, файлъ=файлъ)
    return п.разобрать()


def разобрать_файлъ(путь: str) -> Программа:
    """Удобная обёртка: файлъ → АСД."""
    with open(путь, 'r', encoding='utf-8') as ф:
        кодъ = ф.read()
    return разобрать_кодъ(кодъ, файлъ=путь)


