# ═══════════════════════════════════════
# II. Классъ ошибокъ
# ═══════════════════════════════════════

class ОшибкаП(Exception):
    """Ошибка при транспиляціи кода на языкѣ П."""

    def __init__(self, сообщеніе, строка_номеръ=None, позиція=None, строка_текстъ=None):
        self.сообщеніе = сообщеніе
        self.строка_номеръ = строка_номеръ
        self.позиція = позиція
        self.строка_текстъ = строка_текстъ

    def __str__(self):
        части = [f"\n╔══ ОШИБКА ТРАНСПИЛЯЦІИ ══╗"]
        if self.строка_номеръ is not None:
            части.append(f"║ Строка: {self.строка_номеръ}")
        if self.строка_текстъ is not None:
            части.append(f"║ Содержаніе: {self.строка_текстъ.strip()}")
        if self.позиція is not None:
            части.append(f"║ Позиція: {self.позиція}")
        части.append(f"║ {self.сообщеніе}")
        части.append(f"╚{'═' * 26}╝")
        return '\n'.join(части)


# ═══════════════════════════════════════
# III. Транспайлеръ
# ═══════════════════════════════════════

class ТранспайлеръП:
    """Главный классъ транспайлера П → Python."""

    def __init__(self):
        # Составляемъ регулярныя выраженія для замѣны,
        # сортируя по длинѣ (длинныя сперва, дабы «инако буде» не разбилось)
        self.шаблоны_ключевыхъ = sorted(КЛЮЧЕВЫЯ_СЛОВА.keys(), key=len, reverse=True)
        self.шаблоны_функцій = sorted(ВСТРОЕННЫЯ_ФУНКЦІИ.keys(), key=len, reverse=True)

    def транспилировать(self, исходный_кодъ, имя_файла="<вводъ>"):
        """Транспилировать кодъ на языкѣ П въ Python."""
        строки = исходный_кодъ.split('\n')
        результатъ = []

        for номеръ, строка in enumerate(строки, 1):
            try:
                обработанная = self._обработать_строку(строка, номеръ)
                результатъ.append(обработанная)
            except ОшибкаП:
                raise
            except Exception as e:
                raise ОшибкаП(
                    f"Непредвидѣнная ошибка: {e}",
                    строка_номеръ=номеръ,
                    строка_текстъ=строка
                )

        return '\n'.join(результатъ)

    def _обработать_строку(self, строка, номеръ):
        """Обработать одну строку кода."""

        # Сохраняемъ отступъ
        отступъ = ''
        for символъ in строка:
            if символъ in (' ', '\t'):
                отступъ += символъ
            else:
                break
        содержимое = строка[len(отступъ):]

        # Пустая строка
        if not содержимое.strip():
            return строка

        # Комментарій — не трогаемъ
        if содержимое.strip().startswith('#'):
            return строка

        # Многострочная строка — не трогаемъ
        if содержимое.strip().startswith('"""') or содержимое.strip().startswith("'''"):
            return строка

        # Замѣна операторовъ (до всего прочего!)
        содержимое = self._замѣнить_операторы(содержимое)

        # Обработка спеціальныхъ конструкцій
        содержимое = self._обработать_классъ(содержимое, номеръ)
        содержимое = self._обработать_создатель(содержимое, номеръ)
        содержимое = self._обработать_методъ(содержимое, номеръ)
        содержимое = self._обработать_для_каждаго(содержимое, номеръ)
        содержимое = self._обработать_инако_буде(содержимое, номеръ)
        содержимое = self._обработать_объявить(содержимое, номеръ)
        содержимое = self._обработать_постоянная(содержимое, номеръ)
        содержимое = self._обработать_родительскій(содержимое, номеръ)
        содержимое = self._обработать_привезти(содержимое, номеръ)

        # Замѣна ключевыхъ словъ (общая)
        содержимое = self._замѣнить_ключевыя(содержимое)

        # Замѣна встроенныхъ функцій
        содержимое = self._замѣнить_функціи(содержимое)

        return отступъ + содержимое

    # ─── Спеціальныя обработки ─────────────

    def _обработать_классъ(self, с, номеръ):
        """классъ Имя наслѣдуетъ Родитель: → class Имя(Родитель):"""
        m = re.match(r'^классъ\s+(\S+)\s+наслѣдуетъ\s+(\S+)\s*:', с)
        if m:
            return f"class {m.group(1)}({m.group(2)}):"
        m = re.match(r'^классъ\s+(\S+)\s*:', с)
        if m:
            return f"class {m.group(1)}:"
        return с

    def _обработать_создатель(self, с, номеръ):
        """создатель(...) → def __init__(...)"""
        m = re.match(r'^создатель\s*\(', с)
        if m:
            return 'def __init__(' + с[m.end():]
        return с

    def _обработать_методъ(self, с, номеръ):
        """методъ имя(...) → def имя(...)"""
        m = re.match(r'^методъ\s+', с)
        if m:
            return 'def ' + с[m.end():]
        return с

    def _обработать_для_каждаго(self, с, номеръ):
        """для каждаго X въ Y: → for X in Y:"""
        m = re.match(r'^для\s+каждаго\s+(\S+)\s+въ\s+(.+):', с)
        if m:
            return f"for {m.group(1)} in {m.group(2)}:"
        return с

    def _обработать_инако_буде(self, с, номеръ):
        """инако буде → elif"""
        return re.sub(r'\bинако\s+буде\b', 'elif', с)

    def _обработать_объявить(self, с, номеръ):
        """объявить имя = ... → имя = ..."""
        m = re.match(r'^объявить\s+(.+)', с)
        if m:
            остатокъ = m.group(1)
            if '=' not in остатокъ:
                raise ОшибкаП(
                    "Послѣ «объявить» ожидается присваиваніе (знакъ «=» либо «есть»).",
                    строка_номеръ=номеръ,
                    строка_текстъ=с
                )
            return остатокъ
        return с

    def _обработать_постоянная(self, с, номеръ):
        """постоянная ИМЯ = ... → ИМЯ = ..."""
        m = re.match(r'^постоянная\s+(.+)', с)
        if m:
            остатокъ = m.group(1)
            if '=' not in остатокъ:
                raise ОшибкаП(
                    "Послѣ «постоянная» ожидается присваиваніе (знакъ «=» либо «есть»).",
                    строка_номеръ=номеръ,
                    строка_текстъ=с
                )
            return остатокъ
        return с

    def _обработать_родительскій(self, с, номеръ):
        """родительскій().создатель(...) → super().__init__(...)"""
        с = re.sub(r'родительскій\(\)\.создатель\(', 'super().__init__(', с)
        с = re.sub(r'родительскій\(\)', 'super()', с)
        return с

    def _обработать_привезти(self, с, номеръ):
        """
        привезти модуль → import модуль
        изъ модуль привезти имя какъ алиасъ → from модуль import имя as алиасъ
        """
        m = re.match(r'^изъ\s+(\S+)\s+привезти\s+(.+)', с)
        if m:
            модуль = m.group(1)
            остатокъ = m.group(2).replace(' какъ ', ' as ')
            return f"from {модуль} import {остатокъ}"
        m = re.match(r'^привезти\s+(.+)', с)
        if m:
            остатокъ = m.group(1).replace(' какъ ', ' as ')
            return f"import {остатокъ}"
        return с

    # ─── Замѣна операторовъ ──────────────────

    def _замѣнить_операторы(self, с):
        """Замѣна русскихъ операторовъ на символьные."""
        строковые, с = self._извлечь_строки(с)
        for слово, символъ in ОПЕРАТОРЫ:
            шаблонъ = r'\b' + re.escape(слово) + r'\b'
            с = re.sub(шаблонъ, f' {символъ} ', с)
        # Убираемъ лишніе пробѣлы
        с = re.sub(r'  +', ' ', с).strip()
        с = self._вернуть_строки(с, строковые)
        return с

    # ─── Общія замѣны ──────────────────────

    def _замѣнить_ключевыя(self, с):
        """Замѣна ключевыхъ словъ на Python-эквиваленты."""
        # Защищаемъ строковыя литералы отъ замѣны
        строковые, с = self._извлечь_строки(с)

        for ключ in self.шаблоны_ключевыхъ:
            замѣна = КЛЮЧЕВЫЯ_СЛОВА[ключ]
            if замѣна == '':
                continue  # уже обработано спеціально
            # Замѣняемъ только цѣлыя слова
            шаблонъ = r'\b' + re.escape(ключ) + r'\b'
            с = re.sub(шаблонъ, замѣна, с)

        # Восстанавливаемъ строки
        с = self._вернуть_строки(с, строковые)
        return с

    def _замѣнить_функціи(self, с):
        """Замѣна встроенныхъ функцій."""
        строковые, с = self._извлечь_строки(с)

        for ключ in self.шаблоны_функцій:
            замѣна = ВСТРОЕННЫЯ_ФУНКЦІИ[ключ]
            # Замѣняемъ, только когда за именемъ слѣдуетъ скобка
            шаблонъ = r'\b' + re.escape(ключ) + r'\s*\('
            с = re.sub(шаблонъ, замѣна + '(', с)

        с = self._вернуть_строки(с, строковые)
        return с

    def _извлечь_строки(self, с):
        """Извлечь строковыя литералы и замѣнить заглушками."""
        строковые = []
        шаблонъ = re.compile(r'(\"\"\".*?\"\"\"|\'\'\'.*?\'\'\'|\".*?\"|\'.*?\')', re.DOTALL)
        def замѣна(m):
            строковые.append(m.group(0))
            return f"__СТРОКА_{len(строковые) - 1}__"
        с = шаблонъ.sub(замѣна, с)
        return строковые, с

    def _вернуть_строки(self, с, строковые):
        """Вернуть строковыя литералы на мѣста."""
        for і, строка_лит in enumerate(строковые):
            с = с.replace(f"__СТРОКА_{і}__", строка_лит)
        return с

    # ─── Главныя методы ────────────────────

    def транспилировать_файлъ(self, путь_входа, путь_выхода=None):
        """Транспилировать файлъ .п въ .py."""
        if not os.path.exists(путь_входа):
            raise ОшибкаП(f"Файлъ не найденъ: {путь_входа}")

        with open(путь_входа, 'r', encoding='utf-8') as ф:
            исходный_кодъ = ф.read()

        результатъ = self.транспилировать(исходный_кодъ, имя_файла=путь_входа)

        if путь_выхода is None:
            # Замѣняемъ расширеніе .п на .py
            основа = os.path.splitext(путь_входа)[0]
            путь_выхода = основа + '.py'

        with open(путь_выхода, 'w', encoding='utf-8') as ф:
            ф.write(результатъ)

        return путь_выхода

    def исполнить(self, исходный_кодъ):
        """Транспилировать и тутъ же исполнить кодъ."""
        python_кодъ = self.транспилировать(исходный_кодъ)
        пространство = {}
        exec(python_кодъ, пространство)

    def исполнить_файлъ(self, путь):
        """Транспилировать файлъ и исполнить."""
        if not os.path.exists(путь):
            raise ОшибкаП(f"Файлъ не найденъ: {путь}")
        with open(путь, 'r', encoding='utf-8') as ф:
            исходный_кодъ = ф.read()
        self.исполнить(исходный_кодъ)


# ═══════════════════════════════════════
# IV. Точка входа
# ═══════════════════════════════════════

def главная():
    """Главная функція — обработка аргументовъ командной строки."""
    if len(sys.argv) < 2:
        print("╔══════════════════════════════════════════╗")
        print("║   Транспайлеръ языка «П» → Python        ║")
        print("╠══════════════════════════════════════════╣")
        print("║ Употребленіе:                            ║")
        print("║   python транспайлеръ.py <файлъ.п>       ║")
        print("║     — транспилировать въ .py              ║")
        print("║   python транспайлеръ.py -и <файлъ.п>    ║")
        print("║     — транспилировать и исполнить         ║")
        print("║   python транспайлеръ.py -п <файлъ.п>    ║")
        print("║     — показать Python-кодъ               ║")
        print("╚══════════════════════════════════════════╝")
        sys.exit(1)

    т = ТранспайлеръП()

    режимъ = sys.argv[1]

    try:
        if режимъ == '-и' and len(sys.argv) >= 3:
            # Исполнить
            т.исполнить_файлъ(sys.argv[2])
        elif режимъ == '-п' and len(sys.argv) >= 3:
            # Показать транспилированный кодъ
            with open(sys.argv[2], 'r', encoding='utf-8') as ф:
                кодъ = ф.read()
            print(т.транспилировать(кодъ))
        else:
            # Транспилировать въ файлъ
            путь = sys.argv[1]
            выходъ = т.транспилировать_файлъ(путь)
            print(f"✓ Транспилировано успѣшно: {выходъ}")
    except ОшибкаП as е:
        print(е, file=sys.stderr)
        sys.exit(1)


if __name__ == '__main__':
    главная()
