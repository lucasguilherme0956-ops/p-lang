class ТестПозиціи(unittest.TestCase):
    """Тесты правильности строкъ и позицій."""

    def test_строка_и_столбецъ(self):
        кодъ = 'х есть 5\nу есть 10'
        л = Лексеръ(кодъ)
        токены = л.токенизировать()
        # х должно быть на строкѣ 1, столбцѣ 1
        х = токены[0]
        self.assertEqual(х.строка, 1)
        self.assertEqual(х.столбецъ, 1)
        # у — на строкѣ 2, столбцѣ 1
        у_токены = [т for т in токены
                    if т.типъ == ТипТокена.ИДЕНТИФИКАТОРЪ and т.значеніе == 'у']
        self.assertEqual(len(у_токены), 1)
        self.assertEqual(у_токены[0].строка, 2)


