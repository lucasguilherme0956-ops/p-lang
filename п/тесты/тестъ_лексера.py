def типы(кодъ: str) -> list:
    """Вспомогательная: вернуть списокъ типовъ токеновъ (безъ КОНЕЦЪ)."""
    л = Лексеръ(кодъ)
    токены = л.токенизировать()
    return [т.типъ for т in токены if т.типъ != ТипТокена.КОНЕЦЪ]


def типы_и_значенія(кодъ: str) -> list:
    """Вернуть список паръ (типъ, значеніе), безъ структурныхъ."""
    л = Лексеръ(кодъ)
    токены = л.токенизировать()
    пропустить = {ТипТокена.НОВАЯ_СТРОКА, ТипТокена.ОТСТУПЪ,
                  ТипТокена.ВЫСТУПЪ, ТипТокена.КОНЕЦЪ}
    return [(т.типъ, т.значеніе) for т in токены if т.типъ not in пропустить]


class ТестЛитералы(unittest.TestCase):
    """Тесты числовыхъ и строковыхъ литераловъ."""

    def test_цѣлое(self):
        р = типы_и_значенія('42')
        self.assertEqual(р, [(ТипТокена.ЦѢЛОЕ, '42')])

    def test_вещественное(self):
        р = типы_и_значенія('3.14')
        self.assertEqual(р, [(ТипТокена.ВЕЩЕСТВЕННОЕ, '3.14')])

    def test_строка_двойная(self):
        р = типы_и_значенія('"привѣтъ"')
        self.assertEqual(р, [(ТипТокена.СТРОКА, 'привѣтъ')])

