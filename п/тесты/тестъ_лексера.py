class ТестОтступы(unittest.TestCase):
    """Тесты обработки INDENT / DEDENT."""

    def test_простой_блокъ(self):
        кодъ = 'буде х:\n    у есть 1\nз есть 2'
        р = типы(кодъ)
        self.assertIn(ТипТокена.ОТСТУПЪ, р)
        self.assertIn(ТипТокена.ВЫСТУПЪ, р)

    def test_вложенные_блоки(self):
        кодъ = 'буде а:\n    буде б:\n        х есть 1\n'
        р = типы(кодъ)
        # Два ОТСТУПЪ (0→4→8), два ВЫСТУПЪ (при EOF)
        self.assertEqual(р.count(ТипТокена.ОТСТУПЪ), 2)
        self.assertEqual(р.count(ТипТокена.ВЫСТУПЪ), 2)

    def test_пустыя_строки_не_влияютъ(self):
        кодъ = 'буде х:\n    а есть 1\n\n    б есть 2\n'
        л = Лексеръ(кодъ)
        токены = л.токенизировать()
        # Только одинъ ОТСТУПЪ (пустая строка не закрываетъ блокъ)
        self.assertEqual(
            sum(1 for т in токены if т.типъ == ТипТокена.ОТСТУПЪ), 1)

    def test_комментарій_не_влияетъ(self):
        кодъ = 'буде х:\n    а есть 1\n# комментарій\n    б есть 2\n'
        л = Лексеръ(кодъ)
        токены = л.токенизировать()
        # Только одинъ ОТСТУПЪ
        self.assertEqual(
            sum(1 for т in токены if т.типъ == ТипТокена.ОТСТУПЪ), 1)

    def test_скобки_подавляютъ_отступы(self):
        кодъ = 'ф(\n    1,\n    2\n)'
        л = Лексеръ(кодъ)
        токены = л.токенизировать()
        # Никакихъ ОТСТУПЪ / ВЫСТУПЪ внутри скобокъ
        self.assertNotIn(ТипТокена.ОТСТУПЪ,
                         [т.типъ for т in токены])

    def test_конецъ_файла_закрываетъ_отступы(self):
        кодъ = 'функція ф():\n    х есть 1'
        р = типы(кодъ)
        # ОТСТУПЪ есть, ВЫСТУПЪ генерируется при EOF
        self.assertEqual(р.count(ТипТокена.ОТСТУПЪ), 1)
        self.assertEqual(р.count(ТипТокена.ВЫСТУПЪ), 1)


