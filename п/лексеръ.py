# ═══════════════════════════════════════
# II. Токенъ
# ═══════════════════════════════════════

@dataclass
class Токенъ:
    """Единица лексическаго разбора."""
    типъ: ТипТокена
    значеніе: str
    строка: int
    столбецъ: int

    def __repr__(self):
        if self.типъ in (ТипТокена.НОВАЯ_СТРОКА, ТипТокена.ОТСТУПЪ,
                         ТипТокена.ВЫСТУПЪ, ТипТокена.КОНЕЦЪ):
            return f'{self.типъ.value}:{self.строка}'
        return f'{self.типъ.value}({self.значеніе!r}):{self.строка}:{self.столбецъ}'


