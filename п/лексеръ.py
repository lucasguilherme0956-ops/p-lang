# ═══════════════════════════════════════
# IV. Ошибка лексера
# ═══════════════════════════════════════

class ОшибкаЛексера(Exception):
    """Ошибка, обнаруженная на стадіи лексическаго разбора."""

    def __init__(self, сообщеніе, строка=None, столбецъ=None, файлъ=None):
        self.сообщеніе = сообщеніе
        self.строка = строка
        self.столбецъ = столбецъ
        self.файлъ = файлъ

    def __str__(self):
        мѣсто = ""
        if self.файлъ:
            мѣсто += f"{self.файлъ}, "
        if self.строка is not None:
            мѣсто += f"строка {self.строка}"
        if self.столбецъ is not None:
            мѣсто += f", позиція {self.столбецъ}"
        return f"Ошибка лексера [{мѣсто}]: {self.сообщеніе}"


# ═══════════════════════════════════════
# V. Лексеръ
# ═══════════════════════════════════════

class Лексеръ:
    """
    Лексическій анализаторъ языка П.

    Обрабатываетъ исходный кодъ посимвольно.
    Слѣдитъ за отступами и порождаетъ ОТСТУПЪ / ВЫСТУПЪ.
    Внутри скобокъ отступы и переносы строкъ подавляются.
    """

    def __init__(self, кодъ: str, файлъ: str = '<вводъ>'):
        # Нормализація: убираемъ BOM, приводимъ переносы строкъ
        self.кодъ = кодъ.lstrip('\ufeff').replace('\r\n', '\n').replace('\r', '\n')
        self.файлъ = файлъ
        self.поз = 0
        self.строка = 1
        self.столбецъ = 1
        self.токены: List[Токенъ] = []
        self.стекъ_отступовъ: List[int] = [0]
        self.скобки = 0       # глубина () [] {}
        self.начало_строки = True
        self.ошибки: List[ОшибкаЛексера] = []

    # ─── Навигація по тексту ───────────

    @property
    def _на_концѣ(self) -> bool:
        return self.поз >= len(self.кодъ)

    @property
    def _тек(self) -> str:
        """Текущій символъ (или \\0 на концѣ)."""
        if self._на_концѣ:
            return '\0'
        return self.кодъ[self.поз]

    def _заглянуть(self, на: int = 1) -> str:
        """Посмотрѣть символъ со смѣщеніемъ, не двигаясь."""
        п = self.поз + на
        if п >= len(self.кодъ):
            return '\0'
        return self.кодъ[п]

    def _подвинуть(self) -> str:
        """Прочитать текущій символъ и сдвинуться впередъ."""
        с = self._тек
        self.поз += 1
        if с == '\n':
            self.строка += 1
            self.столбецъ = 1
        else:
            self.столбецъ += 1
        return с

    # ─── Порожденіе токеновъ ───────────

    def _добавить(self, типъ: ТипТокена, значеніе: str = '',
                  строка: int = None, столбецъ: int = None):
        """Добавить токенъ въ списокъ."""
        self.токены.append(Токенъ(
            типъ=типъ,
            значеніе=значеніе,
            строка=строка if строка is not None else self.строка,
            столбецъ=столбецъ if столбецъ is not None else self.столбецъ,
        ))

    def _ошибка(self, сообщеніе: str, строка: int = None, столбецъ: int = None):
        """Зарегистрировать ошибку (не прерывая разборъ)."""
        self.ошибки.append(ОшибкаЛексера(
            сообщеніе,
            строка=строка or self.строка,
            столбецъ=столбецъ or self.столбецъ,
            файлъ=self.файлъ,
        ))

    # ─── Главный методъ ────────────────

    def токенизировать(self) -> List[Токенъ]:
        """
        Разобрать весь исходный кодъ на послѣдовательность токеновъ.

        Возвращаетъ списокъ, оканчивающійся КОНЕЦЪ.
        Ошибки накапливаются въ self.ошибки.
        """
        while not self._на_концѣ:
            # Въ началѣ строки — обрабатываемъ отступъ
            if self.начало_строки:
                self._обработать_отступъ()
                continue

            с = self._тек

            # ─── Новая строка ───
            if с == '\n':
                if self.скобки == 0:
                    self._добавить(ТипТокена.НОВАЯ_СТРОКА, '\\n')
                self._подвинуть()
                self.начало_строки = True
                continue

            # ─── Пробѣлы внутри строки ───
            if с in ' \t':
                self._подвинуть()
                continue

            # ─── Комментарій ───
            if с == '#':
                self._пропустить_комментарій()
                continue

            # ─── Строковый литералъ ───
            if с in '"\'':
                self._читать_строку()
                continue

            # ─── Число ───
            if с.isdigit():
                self._читать_число()
                continue

            # ─── Слово (идентификаторъ / ключевое) ───
            if с.isalpha() or с == '_':
                self._читать_слово()
                continue

            # ─── Знаки ───
            if с in ЗНАКИ:
                self._читать_знакъ()
                continue

            # ─── Неизвѣстное ───
            self._ошибка(f"Неожиданный символъ: «{с}»")
            self._подвинуть()

        # ═══ Конецъ файла ═══
        # Закрываемъ послѣднюю строку
        if self.токены and self.токены[-1].типъ not in (
                ТипТокена.НОВАЯ_СТРОКА, ТипТокена.ВЫСТУПЪ):
            self._добавить(ТипТокена.НОВАЯ_СТРОКА, '\\n')

        # Закрываемъ всѣ открытые отступы
        while len(self.стекъ_отступовъ) > 1:
            self.стекъ_отступовъ.pop()
            self._добавить(ТипТокена.ВЫСТУПЪ, '<ВЫСТУПЪ>')

        self._добавить(ТипТокена.КОНЕЦЪ, '<КОНЕЦЪ>')
        return self.токены

    # ─── Отступы (INDENT / DEDENT) ────

    def _обработать_отступъ(self):
        """
        Измѣрить отступъ въ началѣ строки.
        Породить ОТСТУПЪ при увеличеніи, ВЫСТУПЪ при уменьшеніи.
        Пустыя строки и строки-комментаріи не влияютъ на отступъ.
        """
        self.начало_строки = False

        # Подсчитываемъ ведущіе пробѣлы
        отступъ = 0
        while not self._на_концѣ and self._тек in ' \t':
            if self._тек == ' ':
                отступъ += 1
            else:
                # Табуляція → округляемъ вверхъ до кратнаго 4
                отступъ = (отступъ // 4 + 1) * 4
            self._подвинуть()

        # Пустая строка — пропускаемъ (не влияетъ на отступы)
        if self._на_концѣ or self._тек == '\n':
            if not self._на_концѣ:
                self._подвинуть()  # поглощаемъ \n
                self.начало_строки = True
            return

        # Строка-комментарій — пропускаемъ
        if self._тек == '#':
            self._пропустить_комментарій()
            if not self._на_концѣ and self._тек == '\n':
                self._подвинуть()
                self.начало_строки = True
            return

        # Тройная строка на уровнѣ строки (""") — пропускаемъ какъ комментарій
        if (self._тек in '"\'') and self._заглянуть(1) == self._тек and self._заглянуть(2) == self._тек:
            # Проверяемъ: если тройная строка начинается на уровнѣ строки,
            # она обрабатывается далѣе какъ обычный токенъ
            pass

        # Внутри скобокъ — отступы подавляются
        if self.скобки > 0:
            return

        # Сравниваемъ съ текущимъ уровнемъ
        текущій = self.стекъ_отступовъ[-1]

        if отступъ > текущій:
            self.стекъ_отступовъ.append(отступъ)
            self._добавить(ТипТокена.ОТСТУПЪ, '<ОТСТУПЪ>')

        elif отступъ < текущій:
            while self.стекъ_отступовъ[-1] > отступъ:
                self.стекъ_отступовъ.pop()
                self._добавить(ТипТокена.ВЫСТУПЪ, '<ВЫСТУПЪ>')
            if self.стекъ_отступовъ[-1] != отступъ:
                self._ошибка(
                    f"Несовпаденіе уровня отступа: ожидался {self.стекъ_отступовъ[-1]}, "
                    f"полученъ {отступъ}."
                )

    # ─── Комментаріи ───────────────────

    def _пропустить_комментарій(self):
        """Пропустить всё отъ # до конца строки."""
        while not self._на_концѣ and self._тек != '\n':
            self._подвинуть()

    # ─── Строковые литералы ────────────

    def _читать_строку(self):
        """Прочитать строковый литералъ (" или ', одинарный или тройной)."""
        кавычка = self._тек
        нач_стр = self.строка
        нач_ст = self.столбецъ

        # Тройная кавычка? (""" или ''')
        if self._заглянуть(1) == кавычка and self._заглянуть(2) == кавычка:
            self._читать_тройную(кавычка, нач_стр, нач_ст)
            return

        # Одинарная строка
        self._подвинуть()  # открывающая кавычка
        содержимое = ''

        while not self._на_концѣ:
            с = self._тек

            if с == '\n':
                self._ошибка(
                    "Строка не закрыта до конца строки. "
                    "Употребите тройныя кавычки для многострочныхъ текстовъ.",
                    нач_стр, нач_ст
                )
                break

            if с == кавычка:
                self._подвинуть()  # закрывающая кавычка
                self._добавить(ТипТокена.СТРОКА, содержимое, нач_стр, нач_ст)
                return

            if с == '\\':
                self._подвинуть()
                if not self._на_концѣ:
                    сл = self._тек
                    if   сл == 'n':  содержимое += '\n'
                    elif сл == 't':  содержимое += '\t'
                    elif сл == '\\': содержимое += '\\'
                    elif сл == кавычка: содержимое += кавычка
                    else: содержимое += '\\' + сл
                    self._подвинуть()
                continue

            содержимое += с
            self._подвинуть()

        # Если дошли сюда — строка не закрыта
        self._добавить(ТипТокена.СТРОКА, содержимое, нач_стр, нач_ст)

    def _читать_тройную(self, кавычка: str, нач_стр: int, нач_ст: int):
        """Прочитать тройную строку (\"\"\" или ''')."""
        # Поглощаемъ открывающую тройку
        for _ in range(3):
            self._подвинуть()

        содержимое = ''

        while not self._на_концѣ:
            if (self._тек == кавычка and
                    self._заглянуть(1) == кавычка and
                    self._заглянуть(2) == кавычка):
                # Закрывающая тройка
                for _ in range(3):
                    self._подвинуть()
                self._добавить(ТипТокена.СТРОКА, содержимое, нач_стр, нач_ст)
                return

            содержимое += self._тек
            self._подвинуть()

        self._ошибка(
            "Тройная строка не закрыта до конца файла.",
            нач_стр, нач_ст
        )
        self._добавить(ТипТокена.СТРОКА, содержимое, нач_стр, нач_ст)

    # ─── Числовые литералы ─────────────

    def _читать_число(self):
        """Прочитать цѣлое или вещественное число."""
        нач_стр = self.строка
        нач_ст = self.столбецъ
        начало = self.поз
        вещественное = False

        # Цѣлая часть
        while not self._на_концѣ and self._тек.isdigit():
            self._подвинуть()

        # Дробная часть (только если за точкой слѣдуетъ цифра)
        if not self._на_концѣ and self._тек == '.' and self._заглянуть(1).isdigit():
            вещественное = True
            self._подвинуть()  # точка
            while not self._на_концѣ and self._тек.isdigit():
                self._подвинуть()

        текстъ = self.кодъ[начало:self.поз]
        типъ = ТипТокена.ВЕЩЕСТВЕННОЕ if вещественное else ТипТокена.ЦѢЛОЕ
        self._добавить(типъ, текстъ, нач_стр, нач_ст)

    # ─── Слова ─────────────────────────

    def _читать_слово(self):
        """
        Прочитать слово и опредѣлить его типъ:
          — составное ключевое (не болѣе, въ степени, ...)
          — однословное ключевое (буде, покуда, ...)
          — операторъ-слово (плюсъ, умножить, ...)
          — идентификаторъ
        """
        нач_стр = self.строка
        нач_ст = self.столбецъ
        начало = self.поз

        while not self._на_концѣ and (self._тек.isalnum() or self._тек == '_'):
            self._подвинуть()

        слово = self.кодъ[начало:self.поз]

        # 1) Попытка составного (многословнаго) токена
        if слово in СОСТАВНЫЕ:
            составной = self._попытка_составного(слово)
            if составной is not None:
                типъ, полное = составной
                self._добавить(типъ, полное, нач_стр, нач_ст)
                return

        # 2) Однословное ключевое слово / операторъ
        if слово in КЛЮЧЕВЫЯ_СЛОВА:
            self._добавить(КЛЮЧЕВЫЯ_СЛОВА[слово], слово, нач_стр, нач_ст)
            return

        # 3) Идентификаторъ
        self._добавить(ТипТокена.ИДЕНТИФИКАТОРЪ, слово, нач_стр, нач_ст)

    def _попытка_составного(self, первое: str) -> Optional[Tuple[ТипТокена, str]]:
        """
        Заглянуть впередъ: можно ли соединить два слова въ одинъ токенъ?

        Сохраняетъ позицію. Если второе слово подходитъ — поглощаетъ его.
        Если нѣтъ — откатываетъ.
        """
        сохр_поз = self.поз
        сохр_стр = self.строка
        сохр_ст = self.столбецъ

        # Пропускаемъ пробѣлы (НЕ переносъ строки!)
        while not self._на_концѣ and self._тек in ' \t':
            self._подвинуть()

        # Пытаемся прочитать второе слово
        if not self._на_концѣ and (self._тек.isalpha() or self._тек == '_'):
            начало2 = self.поз
            while not self._на_концѣ and (self._тек.isalnum() or self._тек == '_'):
                self._подвинуть()

            второе = self.кодъ[начало2:self.поз]

            if второе in СОСТАВНЫЕ[первое]:
                типъ = СОСТАВНЫЕ[первое][второе]
                return (типъ, первое + ' ' + второе)

        # Не совпало — откатываемъ на исходную позицію
        self.поз = сохр_поз
        self.строка = сохр_стр
        self.столбецъ = сохр_ст
        return None

    # ─── Знаки препинанія ─────────────

    def _читать_знакъ(self):
        """Прочитать одиночный символъ-раздѣлитель."""
        нач_стр = self.строка
        нач_ст = self.столбецъ
        с = self._подвинуть()

        типъ = ЗНАКИ.get(с)
        if типъ is None:
            self._ошибка(f"Неизвѣстный знакъ: «{с}»", нач_стр, нач_ст)
            return

        # Учитываемъ глубину скобокъ
        if с in '([{':
            self.скобки += 1
        elif с in ')]}':
            self.скобки = max(0, self.скобки - 1)

        self._добавить(типъ, с, нач_стр, нач_ст)


# ═══════════════════════════════════════
# VI. Запускъ какъ скриптъ
# ═══════════════════════════════════════

def форматировать_токены(токены: List[Токенъ]) -> str:
    """Красивый выводъ токеновъ для отладки."""
    строки = []
    тек_строка = -1
    for т in токены:
        if т.строка != тек_строка:
            тек_строка = т.строка
            строки.append(f"\n  строка {тек_строка:3d} │ ")
        if т.типъ == ТипТокена.НОВАЯ_СТРОКА:
            строки.append(' ↵')
        elif т.типъ == ТипТокена.ОТСТУПЪ:
            строки.append(' →ОТСТУПЪ')
        elif т.типъ == ТипТокена.ВЫСТУПЪ:
            строки.append(' ←ВЫСТУПЪ')
        elif т.типъ == ТипТокена.КОНЕЦЪ:
            строки.append(' ■КОНЕЦЪ')
        elif т.типъ == ТипТокена.СТРОКА:
            строки.append(f' "{т.значеніе}"')
        elif т.типъ in (ТипТокена.ЦѢЛОЕ, ТипТокена.ВЕЩЕСТВЕННОЕ):
            строки.append(f' {т.значеніе}')
        elif т.типъ == ТипТокена.ИДЕНТИФИКАТОРЪ:
            строки.append(f' {т.значеніе}')
        else:
            строки.append(f' [{т.типъ.value}]')
    return ''.join(строки)


