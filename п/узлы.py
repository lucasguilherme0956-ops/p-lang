#!/usr/bin/env python3
# -*- coding: utf-8 -*-
"""
═══════════════════════════════════════
  УЗЛЫ АСД (Абстрактное Синтаксическое Дерево)
  языка программированія «П»
═══════════════════════════════════════

Каждый узелъ хранитъ номеръ строки для діагностики.
"""

from dataclasses import dataclass, field
from typing import List, Optional, Any


# ═══════════════════════════════════════
# Базовый узелъ
# ═══════════════════════════════════════

@dataclass
class Узелъ:
    """Базовый классъ для всѣхъ узловъ АСД."""
    строка: int = 0


# ═══════════════════════════════════════
# I. Программа
# ═══════════════════════════════════════

@dataclass
class Программа(Узелъ):
    """Корневой узелъ — вся программа."""
    тѣло: List[Узелъ] = field(default_factory=list)


# ═══════════════════════════════════════
# II. Литералы
# ═══════════════════════════════════════

@dataclass
class ЦѣлоеЧисло(Узелъ):
    значеніе: int = 0

@dataclass
class ВещественноеЧисло(Узелъ):
    значеніе: float = 0.0

@dataclass
class Строка(Узелъ):
    значеніе: str = ''

@dataclass
class Булево(Узелъ):
    значеніе: bool = True

@dataclass
class Ничто(Узелъ):
    pass

@dataclass
class Списокъ(Узелъ):
    """Литералъ списка: [элемъ, элемъ, ...]"""
    элементы: List[Узелъ] = field(default_factory=list)

@dataclass
class Словарь(Узелъ):
    """Литералъ словаря: {ключъ: знач, ...}"""
    пары: List[tuple] = field(default_factory=list)  # [(ключъ, знач), ...]


# ═══════════════════════════════════════
# III. Выраженія
# ═══════════════════════════════════════

@dataclass
class Идентификаторъ(Узелъ):
    имя: str = ''

@dataclass
class БинарнаяОперація(Узелъ):
    """лѣвое <операторъ> правое"""
    лѣвое: Узелъ = None
    операторъ: str = ''
    правое: Узелъ = None

@dataclass
class УнарнаяОперація(Узелъ):
    """<операторъ> операндъ"""
    операторъ: str = ''
    операндъ: Узелъ = None

@dataclass
class ВызовъФункціи(Узелъ):
    """функція(арг1, арг2, ...)"""
    функція: Узелъ = None
    аргументы: List[Узелъ] = field(default_factory=list)

@dataclass
class ДоступъКъАтрибуту(Узелъ):
    """объектъ.атрибутъ"""
    объектъ: Узелъ = None
    атрибутъ: str = ''

@dataclass
class ДоступъПоИндексу(Узелъ):
    """объектъ[индексъ]"""
    объектъ: Узелъ = None
    индексъ: Узелъ = None


# ═══════════════════════════════════════
# IV. Инструкціи (Statements)
# ═══════════════════════════════════════

@dataclass
class Объявленіе(Узелъ):
    """объявить имя есть выраженіе"""
    имя: str = ''
    значеніе: Узелъ = None

@dataclass
class Постоянная(Узелъ):
    """постоянная ИМЯ есть выраженіе"""
    имя: str = ''
    значеніе: Узелъ = None

@dataclass
class Присваиваніе(Узелъ):
    """цѣль есть выраженіе"""
    цѣль: Узелъ = None
    значеніе: Узелъ = None

@dataclass
class ВыраженіеИнструкція(Узелъ):
    """Выраженіе, стоящее само по себѣ (напр. вызовъ функціи)."""
    выраженіе: Узелъ = None


# ═══════════════════════════════════════
# V. Функціи и классы
# ═══════════════════════════════════════

@dataclass
class ОпредѣленіеФункціи(Узелъ):
    """функція имя(параметры): тѣло"""
    имя: str = ''
    параметры: List[str] = field(default_factory=list)
    тѣло: List[Узелъ] = field(default_factory=list)

@dataclass
class Возвратъ(Узелъ):
    """возвратить выраженіе"""
    значеніе: Optional[Узелъ] = None

@dataclass
class ОпредѣленіеКласса(Узелъ):
    """классъ Имя [наслѣдуетъ Родитель]: тѣло"""
    имя: str = ''
    родитель: Optional[str] = None
    тѣло: List[Узелъ] = field(default_factory=list)

@dataclass
class ОпредѣленіеМетода(Узелъ):
    """методъ имя(параметры): тѣло"""
    имя: str = ''
    параметры: List[str] = field(default_factory=list)
    тѣло: List[Узелъ] = field(default_factory=list)

@dataclass
class Создатель(Узелъ):
    """создатель(параметры): тѣло"""
    параметры: List[str] = field(default_factory=list)
    тѣло: List[Узелъ] = field(default_factory=list)


# ═══════════════════════════════════════
# VI. Управленіе потокомъ
# ═══════════════════════════════════════

@dataclass
class ВѣтвьБуде:
    """Одна вѣтвь условія (буде / инако буде)."""
    условіе: Узелъ = None
    тѣло: List[Узелъ] = field(default_factory=list)

@dataclass
class Условіе(Узелъ):
    """буде ... инако буде ... иначе ..."""
    вѣтви: List[ВѣтвьБуде] = field(default_factory=list)
    иначе_тѣло: Optional[List[Узелъ]] = None

@dataclass
class ЦиклъПокуда(Узелъ):
    """покуда условіе: тѣло"""
    условіе: Узелъ = None
    тѣло: List[Узелъ] = field(default_factory=list)

@dataclass
class ЦиклъДля(Узелъ):
    """для перемѣнная въ итерируемое: тѣло"""
    перемѣнная: str = ''
    итерируемое: Узелъ = None
    тѣло: List[Узелъ] = field(default_factory=list)

@dataclass
class Прервать(Узелъ):
    pass

@dataclass
class Продолжить(Узелъ):
    pass

@dataclass
class Пропустить(Узелъ):
    pass


# ═══════════════════════════════════════
# VII. Обработка ошибокъ
# ═══════════════════════════════════════

@dataclass
class Попытка(Узелъ):
    """попытка: ... перехватить имя: ... обязательно: ..."""
    тѣло: List[Узелъ] = field(default_factory=list)
    перехватъ_имя: Optional[str] = None
    перехватъ_тѣло: Optional[List[Узелъ]] = None
    обязательно_тѣло: Optional[List[Узелъ]] = None

@dataclass
class Возбудить(Узелъ):
    """возбудить выраженіе"""
    выраженіе: Узелъ = None


# ═══════════════════════════════════════
# VIII. Модули
# ═══════════════════════════════════════

@dataclass
class Привезти(Узелъ):
    """привезти модуль [какъ алиасъ]"""
    модуль: str = ''
    алиасъ: Optional[str] = None

@dataclass
class ПривезтиИзъ(Узелъ):
    """изъ модуль привезти имя [какъ алиасъ]"""
    модуль: str = ''
    имя: str = ''
    алиасъ: Optional[str] = None


# ═══════════════════════════════════════
# IX. Печать АСД
# ═══════════════════════════════════════

def отобразить_дерево(узелъ, отступъ=0) -> str:
    """Рекурсивное отображеніе дерева для отладки."""
    преф = '  ' * отступъ
    if узелъ is None:
        return преф + 'Ничто'

    имя_класса = type(узелъ).__name__
    строки = []

    if isinstance(узелъ, Программа):
        строки.append(f'{преф}Программа:')
        for и in узелъ.тѣло:
            строки.append(отобразить_дерево(и, отступъ + 1))

    elif isinstance(узелъ, ЦѣлоеЧисло):
        строки.append(f'{преф}Цѣлое({узелъ.значеніе})')
    elif isinstance(узелъ, ВещественноеЧисло):
        строки.append(f'{преф}Вещ({узелъ.значеніе})')
    elif isinstance(узелъ, Строка):
        строки.append(f'{преф}Строка("{узелъ.значеніе}")')
    elif isinstance(узелъ, Булево):
        строки.append(f'{преф}{"Истина" if узелъ.значеніе else "Ложь"}')
    elif isinstance(узелъ, Ничто):
        строки.append(f'{преф}Ничто')
    elif isinstance(узелъ, Списокъ):
        строки.append(f'{преф}Списокъ[{len(узелъ.элементы)}]:')
        for э in узелъ.элементы:
            строки.append(отобразить_дерево(э, отступъ + 1))
    elif isinstance(узелъ, Идентификаторъ):
        строки.append(f'{преф}Ид({узелъ.имя})')

    elif isinstance(узелъ, БинарнаяОперація):
        строки.append(f'{преф}({узелъ.операторъ})')
        строки.append(отобразить_дерево(узелъ.лѣвое, отступъ + 1))
        строки.append(отобразить_дерево(узелъ.правое, отступъ + 1))
    elif isinstance(узелъ, УнарнаяОперація):
        строки.append(f'{преф}Ун({узелъ.операторъ})')
        строки.append(отобразить_дерево(узелъ.операндъ, отступъ + 1))

    elif isinstance(узелъ, ВызовъФункціи):
        строки.append(f'{преф}Вызовъ:')
        строки.append(отобразить_дерево(узелъ.функція, отступъ + 1))
        for а in узелъ.аргументы:
            строки.append(f'{преф}  арг:')
            строки.append(отобразить_дерево(а, отступъ + 2))
    elif isinstance(узелъ, ДоступъКъАтрибуту):
        строки.append(f'{преф}.{узелъ.атрибутъ}')
        строки.append(отобразить_дерево(узелъ.объектъ, отступъ + 1))
    elif isinstance(узелъ, ДоступъПоИндексу):
        строки.append(f'{преф}Индексъ:')
        строки.append(отобразить_дерево(узелъ.объектъ, отступъ + 1))
        строки.append(отобразить_дерево(узелъ.индексъ, отступъ + 1))

    elif isinstance(узелъ, Объявленіе):
        строки.append(f'{преф}Объявить {узелъ.имя} =')
        строки.append(отобразить_дерево(узелъ.значеніе, отступъ + 1))
    elif isinstance(узелъ, Постоянная):
        строки.append(f'{преф}Пост {узелъ.имя} =')
        строки.append(отобразить_дерево(узелъ.значеніе, отступъ + 1))
    elif isinstance(узелъ, Присваиваніе):
        строки.append(f'{преф}Присв:')
        строки.append(отобразить_дерево(узелъ.цѣль, отступъ + 1))
        строки.append(отобразить_дерево(узелъ.значеніе, отступъ + 1))
    elif isinstance(узелъ, ВыраженіеИнструкція):
        строки.append(f'{преф}Выраж:')
        строки.append(отобразить_дерево(узелъ.выраженіе, отступъ + 1))

    elif isinstance(узелъ, ОпредѣленіеФункціи):
        строки.append(f'{преф}Функція {узелъ.имя}({", ".join(узелъ.параметры)}):')
        for и in узелъ.тѣло:
            строки.append(отобразить_дерево(и, отступъ + 1))
    elif isinstance(узелъ, Возвратъ):
        строки.append(f'{преф}Возвратить:')
        if узелъ.значеніе:
            строки.append(отобразить_дерево(узелъ.значеніе, отступъ + 1))
    elif isinstance(узелъ, ОпредѣленіеКласса):
        род = f' наслѣд. {узелъ.родитель}' if узелъ.родитель else ''
        строки.append(f'{преф}Классъ {узелъ.имя}{род}:')
        for и in узелъ.тѣло:
            строки.append(отобразить_дерево(и, отступъ + 1))
    elif isinstance(узелъ, ОпредѣленіеМетода):
        строки.append(f'{преф}Методъ {узелъ.имя}({", ".join(узелъ.параметры)}):')
        for и in узелъ.тѣло:
            строки.append(отобразить_дерево(и, отступъ + 1))
    elif isinstance(узелъ, Создатель):
        строки.append(f'{преф}Создатель({", ".join(узелъ.параметры)}):')
        for и in узелъ.тѣло:
            строки.append(отобразить_дерево(и, отступъ + 1))

    elif isinstance(узелъ, Условіе):
        for і, в in enumerate(узелъ.вѣтви):
            мет = 'Буде' if і == 0 else 'ИнакоБуде'
            строки.append(f'{преф}{мет}:')
            строки.append(отобразить_дерево(в.условіе, отступъ + 1))
            for и in в.тѣло:
                строки.append(отобразить_дерево(и, отступъ + 1))
        if узелъ.иначе_тѣло:
            строки.append(f'{преф}Иначе:')
            for и in узелъ.иначе_тѣло:
                строки.append(отобразить_дерево(и, отступъ + 1))
    elif isinstance(узелъ, ЦиклъПокуда):
        строки.append(f'{преф}Покуда:')
        строки.append(отобразить_дерево(узелъ.условіе, отступъ + 1))
        for и in узелъ.тѣло:
            строки.append(отобразить_дерево(и, отступъ + 1))
    elif isinstance(узелъ, ЦиклъДля):
        строки.append(f'{преф}Для {узелъ.перемѣнная} въ:')
        строки.append(отобразить_дерево(узелъ.итерируемое, отступъ + 1))
        for и in узелъ.тѣло:
            строки.append(отобразить_дерево(и, отступъ + 1))

    elif isinstance(узелъ, Прервать):
        строки.append(f'{преф}Прервать')
    elif isinstance(узелъ, Продолжить):
        строки.append(f'{преф}Продолжить')
    elif isinstance(узелъ, Пропустить):
        строки.append(f'{преф}Пропустить')

    elif isinstance(узелъ, Попытка):
        строки.append(f'{преф}Попытка:')
        for и in узелъ.тѣло:
            строки.append(отобразить_дерево(и, отступъ + 1))
        if узелъ.перехватъ_тѣло is not None:
            строки.append(f'{преф}Перехватить {узелъ.перехватъ_имя or ""}:')
            for и in узелъ.перехватъ_тѣло:
                строки.append(отобразить_дерево(и, отступъ + 1))
        if узелъ.обязательно_тѣло is not None:
            строки.append(f'{преф}Обязательно:')
            for и in узелъ.обязательно_тѣло:
                строки.append(отобразить_дерево(и, отступъ + 1))
    elif isinstance(узелъ, Возбудить):
        строки.append(f'{преф}Возбудить:')
        строки.append(отобразить_дерево(узелъ.выраженіе, отступъ + 1))

    elif isinstance(узелъ, Привезти):
        ал = f' какъ {узелъ.алиасъ}' if узелъ.алиасъ else ''
        строки.append(f'{преф}Привезти {узелъ.модуль}{ал}')
    elif isinstance(узелъ, ПривезтиИзъ):
        ал = f' какъ {узелъ.алиасъ}' if узелъ.алиасъ else ''
        строки.append(f'{преф}Изъ {узелъ.модуль} привезти {узелъ.имя}{ал}')

    else:
        строки.append(f'{преф}{имя_класса}(?)')

    return '\n'.join(строки)
